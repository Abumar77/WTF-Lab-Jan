import 'package:flutter/material.dart';

class CustomTheme {
  final Color selectedMsg;
  final Color unselectedMsg;
  final Color dataLabel;
  final Color calendar;
  final Color helpWindow;
  final Color bot;
  final Color backgroundCategory;
  final ThemeData appTheme;

  CustomTheme({
    this.selectedMsg,
    this.unselectedMsg,
    this.dataLabel,
    this.calendar,
    this.helpWindow,
    this.bot,
    this.backgroundCategory,
    this.appTheme,
  });
}
